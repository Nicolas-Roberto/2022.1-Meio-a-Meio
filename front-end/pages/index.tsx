import type { NextPage } from "next";
import Head from "next/head";
import Image from "next/image";
import styles from "../styles/Home.module.css";
import Typography from "@mui/material/Typography";
import Link from "@mui/material/Link";
import { createTheme, ThemeProvider } from "@mui/material/styles";
import React, { useState, ChangeEvent } from "react";
import Container from "@mui/material/Container";
import CssBaseline from "@mui/material/CssBaseline";
import Box from "@mui/material/Box";
import Grid from "@mui/material/Grid";
import TextField from "@mui/material/TextField";
import Button from "@mui/material/Button";
import FormControlLabel from "@mui/material/FormControlLabel";
import Checkbox from "@mui/material/Checkbox";
import { useRouter } from "next/router";
import IconButton from "@material-ui/core/IconButton";
import InputLabel from "@material-ui/core/InputLabel";
import Visibility from "@material-ui/icons/Visibility";
import InputAdornment from "@material-ui/core/InputAdornment";
import VisibilityOff from "@material-ui/icons/VisibilityOff";
import Input from "@material-ui/core/Input";
import FormHelperText from '@mui/material/FormHelperText';
import FormControl from '@mui/material/FormControl';

function Copyright(props: any) {
  return (
    <Typography
      variant="body2"
      color="text.secondary"
      align="center"
      {...props}
    >
      {"Copyright © "}
      <Link
        color="inherit"
        href="https://github.com/mdsreq-fga-unb/2022.1-Meio-a-Meio"
      >
        Meio a Meio
      </Link>{" "}
      {new Date().getFullYear()}
      {"."}
    </Typography>
  );
}

const theme = createTheme();

const Home: NextPage = () => {
  const [data, setData] = useState<any>({});
  const [errors, setErrors] = useState<any>({});
  const router = useRouter();
  const [values, setValues] = React.useState({
    password: "",
    showPassword: false,
  });

  const handleClickShowPassword = () => {
    setValues({ ...values, showPassword: !values.showPassword });
  };

  const handleMouseDownPassword = (event) => {
    event.preventDefault();
  };

  const handlePasswordChange = (e: ChangeEvent<HTMLInputElement>) => {
    setData({ ...data, [e.target.name]: e.target.value });
    let tempErrors = errors;
    delete tempErrors[e.target.name];
    setErrors(tempErrors);
  };

  const handleText = (e: ChangeEvent<HTMLInputElement>) => {
    const clearText = e.target.value.replace(/\d/, "");
    setData({ ...data, [e.target.name]: clearText });
    let tempErrors = errors;
    delete tempErrors[e.target.name];
    setErrors(tempErrors);
  };
  const handleCheckData = () => {
    const { matricula, senha } = data;
    let emptyFields: any = {};

    if (!matricula || matricula.length === 0) {
      emptyFields.matricula = "O campo de matrícula não pode ser vazio";
    }
    if (!senha || senha.length === 0) {
      emptyFields.senha = "O campo de senha não pode ser vazio";
    }
    if (Object.keys(emptyFields).length > 0) {
      setErrors(emptyFields);
      return 1;
    }
    return 0;
  };

  const handleSubmit = (event: React.FormEvent<HTMLFormElement>) => {
    event.preventDefault();
    if (handleCheckData()) {
      return;
    }
    router.push("/docente/portal");
  };
  return (
    <ThemeProvider theme={theme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: "flex",
            flexDirection: "column",
            alignItems: "center",
          }}
        >
          <div>
            <Head>
              <title>Galdi</title>
              <meta name="description" content="Generated by meio a meio" />
              <link rel="icon" href="/images/icon.png" />
            </Head>
            <h3 className={styles.title}>Bem vindo(a) ao Galdi!</h3>
          </div>
          <Typography component="h1" variant="h5">
            Entrar no Sistema
          </Typography>
          <Box
            component="form"
            onSubmit={handleSubmit}
            noValidate
            sx={{ mt: 2.5 }}
          >
              <FormControl sx={{ m: 1 }} variant="standard" fullWidth>
             <InputLabel htmlFor="standard-adornment-password">Matrícula</InputLabel>
             <Input
               
              required
              name="matricula"
              onChange={handleText}
              value={data ? data.matricula : ""}
              error={errors.matricula ? true : false}
            />
            <FormHelperText error>{errors.matricula}</FormHelperText>
            </FormControl>
            <FormControl sx={{ m: 1 }} variant="standard" fullWidth>
            <InputLabel htmlFor="standard-adornment-password">Senha</InputLabel>
            <Input
            fullWidth
            required
            id="senha"
            name="senha"
            type={values.showPassword ? 'text' : 'password'}
            value={data ? data.senha : ""}
            error={errors.senha ? true : false}
            onChange={handlePasswordChange}
            endAdornment={
              <InputAdornment position="end">
                <IconButton
                  aria-label="toggle password visibility"
                  onClick={handleClickShowPassword}
                  onMouseDown={handleMouseDownPassword}
                >
                  {values.showPassword ? <VisibilityOff /> : <Visibility />}
                </IconButton>
              </InputAdornment>
            }
            />
            <FormHelperText error>{errors.senha}</FormHelperText>
            </FormControl>
            <FormControlLabel
              sx={{ m: 1 }}
              control={<Checkbox value="remember" color="primary" />}
              label="Lembre-se de mim"
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Entrar
            </Button>
            <Grid container>
              <Grid item xs>
                <Link href="/professor/esqueceuASenha" variant="body2" sx={{ m: 1 }}>
                  Esqueceu a senha?
                </Link>
              </Grid>
              <Grid item>
                <Link href="/docente/cadastro" variant="body2" sx={{ m: 1.5 }}>
                  {"Não possui uma conta? Inscreva-se"}
                </Link>
              </Grid>
            </Grid>
            <Grid container justifyContent="center"></Grid>
          </Box>
        </Box>
        <Copyright sx={{ mt: 8, mb: 4 }} />
      </Container>
    </ThemeProvider>
  );
};

export default Home;
